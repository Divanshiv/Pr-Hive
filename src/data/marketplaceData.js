// Traffic growth history (6 months)
export const generateTrafficHistory = (currentTraffic, growthRate) => {
  const months = ['Sep', 'Oct', 'Nov', 'Dec', 'Jan', 'Feb'];
  const currentMonthIndex = 5;
  const data = [];
  
  for (let i = 0; i < months.length; i++) {
    const monthsFromCurrent = currentMonthIndex - i;
    const growthMultiplier = Math.pow(1 - growthRate / 100, monthsFromCurrent);
    data.unshift({
      month: months[i],
      value: Math.round(currentTraffic * growthMultiplier)
    });
  }
  
  return data;
};
